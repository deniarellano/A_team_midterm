#There is a problem with this analysis - 'Tier 1' & 'Tier 2' stations are not being recorded because Northbound and Southbound stations are counted as two separate stations so many Tier 1 stations have lesser than 72 in Northbound and lesser than 72 in Southbound

#Create a new column for SB 79 tier classifications
cleaned_stations_df['SB79_tier'] = ''
#Transit types not included in tier 1
