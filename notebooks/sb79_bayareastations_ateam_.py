tier_2_mask = (cleaned_stations_df['number_of_arrivals'] >= 48) & (cleaned_stations_df['number_of_arrivals'] < 72)

na_mask = cleaned_stations_df['number_of_arrivals'] < 48 #Stations not applicable to SB79

