"""**Classify as Tier-1 and Tier-2 stations under SB79**

Criteria for classification:

**Tier 1**: Atleast 72 trains per day(heavy rail  or high frequency commuter rail in urban transit counties)

**Tier 2**: 48 - 72 trains per day(light rail transit, by high-frequency commuter rail, or by bus rapid transit)


"""

transit_not_included = ['Cable Tram','Bus/Cable Tram','Ferry']
